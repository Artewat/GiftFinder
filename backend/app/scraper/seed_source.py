@register("seed")
class SeedSource(FeedSource):
    source_name = "seed"

    def __init__(self, source_name: str = "seed") -> None:
        self.source_name = source_name

    def categories(self) -> Iterator[FeedCategory]:
        for ext_id, name, parent in _CATEGORIES:
            yield FeedCategory(external_id=ext_id, name=name,
                               parent_external_id=parent)

    def offers(self) -> Iterator[FeedOffer]:
        for ext_id, name, price, cat, pic, desc in _OFFERS:
            yield FeedOffer(
                external_id=ext_id,
                name=name,
                price=float(price),
                currency="RUB",
                url=f"https://example-shop.local/product/{ext_id}",
                category_external_id=cat,
                picture=pic,
                description=desc,
            )
